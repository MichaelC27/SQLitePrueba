        obj_usuarios.setNombre(txtNombre.getText().trim());
        obj_usuarios.setApellido(txtApellido.getText().trim());

        // Obtener una conexi贸n a la base de datos
        Connection conexion = Conexion.obtenerConexion();

        // Insertar el objeto obj_usuarios en la base de datos
